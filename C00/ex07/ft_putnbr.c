#include <unistd.h>

void	ft_putnbr(int n)
{
	char	p;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(1, "-2147483648", 11);
			return ;
		}
		else
		{
			write(1, "-", 1);
			n = ~(n) + 1;
			ft_putnbr(n);
			return ;
		}
	}
	if (n > 9)
		ft_putnbr(n / 10);
	p = (n % 10) + 48;
	write(1, &p, 1);
}
