#include <stddef.h>

char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	if (*to_find == '\0')
		return (str);
	i = 0;
	while (str[i])
	{
		j = 0;
		while (str[i + j] == to_find[j])
		{
			if (to_find[j + 1] == '\0')
				return (&str[i]);
			j++;
		}
		i++;
	}
	return (NULL);
}

int	to_find_len(char *to_find)
{
	int	substring_size;

	substring_size = 0;
	while (to_find[substring_size])
	{
		substring_size++;
	}
	return (substring_size);
}
