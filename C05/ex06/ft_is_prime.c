int	ft_is_prime(int nb)
{
	int	i;
	int	div;

	if (nb <= 1)
		return (0);
	i = 2;
	div = 0;
	while (i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
