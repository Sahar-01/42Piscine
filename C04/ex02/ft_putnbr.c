#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	p;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	p = (nb % 10) + '0';
	write(1, &p, 1);
}
